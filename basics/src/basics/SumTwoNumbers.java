package basics;

public class SumTwoNumbers {
    public static void main(String[] args) {
        final int num1 = 0; // initializing a constant num2 with the value 2
        final int num2 = 2; // initializing a constant num2 with the value 2

        int sum = num1 + num2; // calculates the sum of the numbers and stores the value in a variable sum

        System.out.println(sum); // prints the sum of num1 and num2
    } // end of main
} // end of class
